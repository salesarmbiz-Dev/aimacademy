// Helper function to get challenges by category
export const getChallengesByCategory = (category: SpotCategory): SpotChallengeData[] => {
  return SPOT_CHALLENGES.filter(c => c.category === category);
};

// Helper function to get random challenge
export const getRandomChallenge = (excludeIds: string[] = []): SpotChallengeData | null => {
  const available = SPOT_CHALLENGES.filter(c => !excludeIds.includes(c.id));
  if (available.length === 0) return null;
  return available[Math.floor(Math.random() * available.length)];
};

// Calculate XP with streak bonus
export const calculateXP = (correct: boolean, streak: number, usedHint: boolean): number => {
  if (!correct) return 0;
  
  let baseXP = 50;
  
  // Streak bonus
  if (streak >= 20) baseXP *= 2;
  else if (streak >= 10) baseXP *= 1.5;
  else if (streak >= 5) baseXP *= 1.25;
  else if (streak >= 3) baseXP *= 1.1;
  
  // Hint penalty
  if (usedHint) baseXP = Math.max(10, baseXP - 10);
  
  return Math.round(baseXP);
};
